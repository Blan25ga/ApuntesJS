
// parseInt(), con una base.


// parseInt(string, base)

parseInt("11", 2); // retorna 3, porque 11 en binario es 3 en decimal

console.log(parseInt("110111", 2)); // 55, porque 110111 en binario es 55 en decimal
console.log(parseInt("3E0A", 16)); // 15882, porque 3E0A en hexadecimal es 15882 en decimal